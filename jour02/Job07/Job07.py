def print_developer_type(language):
    if language == "javascript":
        print("tu es un développeur web")
    elif language == "python":
        print("tu es un développeur IA")
    elif language == "java":
        print("tu es un développeur logiciel")
    elif language == "reactjs":
        print("tu es un développeur mobile")
    else:
        print("un jour, je serai le meilleur développeur...")
print_developer_type("javascript")
print_developer_type("python")
print_developer_type("java")
print_developer_type("reactjs")
print_developer_type("c++")